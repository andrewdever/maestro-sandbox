/**
 * Tier 3: NVIDIA OpenShell sandbox plugin.
 *
 * Runs code in OpenShell sandboxes — K3s-in-Docker with a 4-layer policy
 * engine (filesystem, network, process, inference) and Privacy Router.
 *
 * Requires OpenShell CLI (`openshell`) to be installed and running.
 * The plugin translates maestro SandboxConfig into OpenShell's YAML policy
 * format, creates sandboxes via the CLI, and executes code within them.
 *
 * Architecture (from tech review):
 *   Gateway (control plane) → Sandbox (isolated container) →
 *   Policy Engine (Landlock + seccomp + netns) → Privacy Router
 *
 * Feature-flagged: must be explicitly enabled in configuration.
 * Default Tier 3 remains Docker. Shadow mode recommended before graduation.
 *
 * References:
 *   - github.com/NVIDIA/OpenShell
 *   - docs/specs/review-nemoclaw-openshell.html
 *   - Security spec §14, trust level enforcement
 */

