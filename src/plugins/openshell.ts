type SandboxExecResult = { stdout: string; stderr: string };
type SandboxExecFn = (args: string[], options?: { timeout?: number; input?: string }) => Promise<SandboxExecResult>;

// ---------------------------------------------------------------------------
// Plugin Implementation
// ---------------------------------------------------------------------------

const openshellPlugin: SandboxPlugin = {
  name: 'openshell',
  version: '0.0.1',
  requiredCoreVersion: '>=0.0.1',
  isolationLevel: 'container',

  async create(config: SandboxConfig): Promise<Sandbox> {
    // 0. Validate config — programming errors checked before env checks
    if (config.hostFunctions && Object.keys(config.hostFunctions).length > 0) {
      throw new SandboxCrashError(
        'OpenShell plugin does not support hostFunctions in V1. Use isolated-vm for host callbacks.',
      );
    }

    // 1. Verify OpenShell CLI is available
    try {
      await execFileAsync('openshell', ['version'], { timeout: 5000 });
    } catch {
      throw new SandboxCrashError(
        'OpenShell CLI is not available. Install from github.com/NVIDIA/OpenShell or use degradation chain.',
      );
    }

    const sandboxId = `maestro-${randomUUID()}`;
    const workDir = '/sandbox/work';
    let destroyed = false;
    let sandboxCreated = false;

    // 2. Generate 4-layer policy from SandboxConfig
    const policy = buildPolicy(config);
    const policyYaml = policyToYaml(policy);
    const policyDir = join(tmpdir(), `maestro-openshell-${sandboxId}`);
    const policyPath = join(policyDir, 'policy.yaml');

    // 3. Write secrets to env file (NemoClaw pattern: credential-as-env-var, never to disk)
    let envFilePath: string | undefined;
    try {
      await mkdir(policyDir, { recursive: true });
      await writeFile(policyPath, policyYaml, { mode: 0o600 });

      if (config.secrets && Object.keys(config.secrets).length > 0) {
        envFilePath = join(policyDir, 'env');
        const lines = Object.entries(config.secrets)
          .map(([k, v]) => `${k}=${v}`)
          .join('\n');
        await writeFile(envFilePath, lines, { mode: 0o600 });
      }

      // 4. Create OpenShell sandbox
      const createArgs = [
        'sandbox', 'create',
        '--name', sandboxId,
        '--policy', policyPath,
        '--memory', `${config.limits.memoryMB}m`,
        '--timeout', `${config.limits.timeoutMs}ms`,
        '--version', OPENSHELL_VERSION,
      ];

      if (envFilePath) {
        createArgs.push('--env-file', envFilePath);
      }

      if (!config.limits.networkAccess) {
        createArgs.push('--network', 'none');
      }

      await execFileAsync('openshell', createArgs, { timeout: CREATE_TIMEOUT_MS });
      sandboxCreated = true;
    } catch (err) {
      // Clean up policy directory on creation failure — caller never gets
      // a sandbox reference so destroy() will never be called.
      await rm(policyDir, { recursive: true, force: true }).catch(() => {});
      throw new SandboxCrashError(
        `Failed to create OpenShell sandbox: ${err instanceof Error ? err.message : String(err)}`,
      );
    } finally {
      // Clean up secrets from disk immediately (even on success)
      if (envFilePath) {
        await rm(envFilePath, { force: true }).catch(() => {});
      }
    }

    // Helper: execute a command inside the OpenShell sandbox
    async function sandboxExec(
      args: string[],
      options?: { timeout?: number; input?: string },
    ): Promise<SandboxExecResult> {
      const execArgs = ['sandbox', 'exec', '--name', sandboxId, '--', ...args];
      const result = await execFileAsync('openshell', execArgs, {
        timeout: options?.timeout ?? config.limits.timeoutMs + 2000,
        maxBuffer: 10 * 1024 * 1024,
        ...(options?.input ? { input: options.input } : {}),
      } as never);
      return { stdout: String(result.stdout), stderr: String(result.stderr ?? '') };
    }

    const sandbox: Sandbox = {
      async execute(code: string, options?: ExecuteOptions): Promise<SandboxResult> {
        if (destroyed) {
          return {
            success: false,
            error: 'Sandbox has been destroyed',
            logs: [],
            metrics: { cpuMs: 0, memoryMB: 0, wallMs: 0 },
          };
        }

        const startTime = Date.now();
        const logs: string[] = [];

        try {
          // Write script to sandbox filesystem (avoids shell injection)
          const scriptName = `exec-${randomUUID()}.mjs`;
          const scriptPath = `/sandbox/${scriptName}`;
          const wrappedCode = buildScript(code, options);

          await sandboxExec(
            ['sh', '-c', `cat > ${shellEscape(scriptPath)}`],
            { timeout: 5000, input: wrappedCode },
          );

          // Execute script with memory limit
          const result = await sandboxExec(
            ['node', `--max-old-space-size=${Number(config.limits.memoryMB)}`, scriptPath],
            { timeout: config.limits.timeoutMs + 2000 },
          );

          const wallMs = Date.now() - startTime;
          const metrics: SandboxMetrics = { cpuMs: wallMs, memoryMB: 0, wallMs };

          const parsed = parseScriptOutput(result.stdout);
          if (parsed) {
            logs.push(...parsed.logs);
            if (result.stderr.trim()) logs.push(result.stderr.trim());
            if (parsed.error) {
              return { success: false, error: parsed.error, logs, metrics };
            }
            return { success: true, result: parsed.result, logs, metrics };
          }

          if (result.stdout.trim()) logs.push(result.stdout.trim());
          if (result.stderr.trim()) logs.push(result.stderr.trim());
          return { success: true, result: undefined, logs, metrics };
        } catch (err: unknown) {
          const wallMs = Date.now() - startTime;
          const metrics: SandboxMetrics = { cpuMs: wallMs, memoryMB: 0, wallMs };

          if (err && typeof err === 'object' && 'killed' in err && err.killed) {
            return { success: false, error: new SandboxTimeoutError(), logs, metrics };
          }

          const parsedErr = parseExecError(err);
          if (parsedErr) {
            logs.push(...parsedErr.logs);
            if (parsedErr.error) {
              return { success: false, error: parsedErr.error, logs, metrics };
            }
          }

          const msg = err instanceof Error ? err.message : String(err);

          if (msg.includes('ETIMEDOUT') || msg.includes('timed out') || msg.includes('deadline exceeded')) {
            return { success: false, error: new SandboxTimeoutError(), logs, metrics };
          }

          // OpenShell returns exit code 137 for OOM (K8s OOMKilled)
          const exitCode = err && typeof err === 'object' && 'code' in err ? (err as { code: unknown }).code : undefined;
          if (msg.includes('OOM') || msg.includes('out of memory') || msg.includes('OOMKilled') || exitCode === 137) {
            return { success: false, error: new SandboxOOMError(), logs, metrics };
          }

          return { success: false, error: msg, logs, metrics };
        }
      },

      async *executeStream(code: string, options?: ExecuteOptions): AsyncIterable<SandboxChunk> {
        const result = await sandbox.execute(code, options);
        for (const log of result.logs) {
          yield { stream: 'stdout', data: log, timestamp: Date.now() };
        }
        if (!result.success && result.error) {
          const msg = typeof result.error === 'string' ? result.error : result.error.message;
          yield { stream: 'stderr', data: msg, timestamp: Date.now() };
        }
      },

      fs: createOpenShellFileAccess(sandboxId, sandboxExec),
      git: createOpenShellGitAccess(sandboxId, workDir, sandboxExec),

      async ready(): Promise<boolean> {
        if (destroyed || !sandboxCreated) return false;
        try {
          const result = await sandboxExec(['echo', 'ready'], { timeout: 5000 });
          return result.stdout.trim() === 'ready';
        } catch {
          return false;
        }
      },

      async destroy(): Promise<void> {
        if (destroyed) return;
        destroyed = true;
        if (sandboxCreated) {
          try {
            await execFileAsync(
              'openshell',
              ['sandbox', 'delete', '--name', sandboxId, '--force'],
              { timeout: 10000 },
            );
          } catch {
            // Sandbox may already be gone
          }
        }
        // Clean up policy directory
        await rm(policyDir, { recursive: true, force: true }).catch(() => {});
      },
    };

    return sandbox;
  },
};

