const SANDBOX_BASE = '/sandbox';

function createOpenShellFileAccess(
  _sandboxId: string,
  sandboxExec: SandboxExecFn,
): SandboxFileAccess {
  return {
    async read(path: string): Promise<string> {
      const safePath = confinePath(SANDBOX_BASE, path);
      const result = await sandboxExec(['cat', safePath], { timeout: 5000 });
      return result.stdout;
    },

    async write(path: string, content: string): Promise<void> {
      const safePath = confinePath(SANDBOX_BASE, path);
      const dir = safePath.split('/').slice(0, -1).join('/');
      await sandboxExec(['mkdir', '-p', dir], { timeout: 5000 });
      await sandboxExec(
        ['sh', '-c', `cat > ${shellEscape(safePath)}`],
        { timeout: 5000, input: content },
      );
    },

    async list(dir: string): Promise<string[]> {
      const safePath = confinePath(SANDBOX_BASE, dir);
      const result = await sandboxExec(['ls', safePath], { timeout: 5000 });
      return result.stdout.trim().split('\n').filter(Boolean);
    },
  };
}

// ---------------------------------------------------------------------------
// Git Access
// ---------------------------------------------------------------------------

function createOpenShellGitAccess(
  sandboxId: string,
  workDir: string,
  sandboxExec: SandboxExecFn,
): SandboxGitAccess {
  let initialized = false;

  async function ensureGit(): Promise<void> {
    if (initialized) return;
    await sandboxExec(['mkdir', '-p', workDir]);
    await sandboxExec(['git', '-C', workDir, 'init']);
    await sandboxExec(['git', '-C', workDir, 'config', 'user.email', 'sandbox@maestro.dev']);
    await sandboxExec(['git', '-C', workDir, 'config', 'user.name', 'Maestro Sandbox']);
    await sandboxExec(['git', '-C', workDir, 'add', '-A']);
    await sandboxExec(['git', '-C', workDir, 'commit', '-m', 'initial', '--allow-empty']);
    initialized = true;
  }

  return {
    async inject(source: string | Buffer): Promise<void> {
      await ensureGit();
      if (typeof source === 'string') {
        // Copy tarball into sandbox and extract
        // OpenShell uses `openshell sandbox cp` for file transfer
        await execFileAsync(
          'openshell',
          ['sandbox', 'cp', '--name', sandboxId, source, `${workDir}/`],
          { timeout: 10000 },
        );
        const filename = source.split('/').pop()!;
        await sandboxExec(['tar', 'xf', `${workDir}/${filename}`, '-C', workDir]);
        await sandboxExec(['rm', `${workDir}/${filename}`]);
      } else {
        throw new Error('Buffer injection not yet supported in OpenShell plugin — use file path');
      }
      await sandboxExec(['git', '-C', workDir, 'add', '-A']);
      await sandboxExec(['git', '-C', workDir, 'commit', '-m', 'injected', '--allow-empty']);
    },

    async exportPatch(): Promise<string> {
      await ensureGit();
      const result = await sandboxExec(['git', '-C', workDir, 'diff', 'HEAD']);
      return result.stdout;
    },

    async exportFiles(paths: string[]): Promise<Buffer> {
      await ensureGit();
      for (const p of paths) {
        confinePath(workDir, p);
      }
      const tarName = '.maestro-export.tar';
      await sandboxExec(['tar', 'cf', `${workDir}/${tarName}`, '-C', workDir, ...paths]);
      // Use base64 encoding to safely transfer binary tar data through text pipes
      const result = await sandboxExec(
        ['sh', '-c', `base64 ${shellEscape(`${workDir}/${tarName}`)}`],
        { timeout: 10000 },
      );
      await sandboxExec(['rm', `${workDir}/${tarName}`]);
      return Buffer.from(result.stdout.trim(), 'base64');
    },
  };
}

