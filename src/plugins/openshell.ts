/** OpenShell image version pin. Update requires full contract test re-run. */
const OPENSHELL_VERSION = '0.4.0';

/** Default creation timeout — K3s pod scheduling adds latency vs raw Docker. */
const CREATE_TIMEOUT_MS = 30_000;

/** Max idle memory per sandbox (tech review: must be <512MB to graduate). */
const MAX_IDLE_MEMORY_MB = 512;

// ---------------------------------------------------------------------------
// Policy Generation
// ---------------------------------------------------------------------------

/**
 * OpenShell policy layers generated from SandboxConfig.
 *
 * Each layer maps to one of OpenShell's 4 policy dimensions:
 *   - filesystem: read-only root, tmpfs mounts, path restrictions
 *   - network: egress rules, L7 filtering, DNS pinning
 *   - process: capability restrictions, seccomp profile, PID limits
 *   - inference: Privacy Router config, model routing
 *
 * Generated from sandbox config — never hand-authored.
 */
export interface OpenShellPolicy {
  filesystem: {
    readOnly: boolean;
    tmpfsMounts: string[];
    allowedPaths: string[];
  };
  network: {
    egress: 'none' | 'filtered' | 'unrestricted';
    allowedHosts?: string[];
    dnsPolicy: 'none' | 'restricted';
  };
  process: {
    capabilities: string[];
    seccomp: 'strict' | 'default';
    pidsLimit: number;
    noNewPrivileges: boolean;
  };
  inference: {
    privacyRouter: boolean;
    stripCredentials: boolean;
    allowedProviders?: string[];
  };
}

/**
 * Translate a SandboxConfig into OpenShell's 4-layer policy.
 */
export function buildPolicy(config: SandboxConfig): OpenShellPolicy {
  return {
    filesystem: {
      // Root is always read-only — writes go through tmpfs overlays (matches Docker --read-only pattern)
      readOnly: true,
      tmpfsMounts: config.limits.filesystemAccess !== 'none'
        ? ['/sandbox:rw,nosuid,size=256m', '/tmp:rw,noexec,nosuid,size=64m']
        : [],
      allowedPaths: config.limits.filesystemAccess !== 'none' ? ['/sandbox'] : [],
    },
    network: {
      egress: config.limits.networkAccess ? 'filtered' : 'none',
      allowedHosts: config.network?.allowedPeers,
      dnsPolicy: config.limits.networkAccess ? 'restricted' : 'none',
    },
    process: {
      capabilities: [], // Drop ALL — NemoClaw pattern
      seccomp: 'strict',
      pidsLimit: 64,
      noNewPrivileges: true,
    },
    inference: {
      privacyRouter: true,
      stripCredentials: true,
      allowedProviders: undefined, // All providers allowed by default
    },
  };
}

/**
 * Serialize policy to YAML format that OpenShell's policy engine expects.
 */
export function policyToYaml(policy: OpenShellPolicy): string {
  const lines: string[] = [
    '# Auto-generated by maestro — do not edit',
    `# Generated: ${new Date().toISOString()}`,
    '',
    'filesystem:',
    `  read_only: ${policy.filesystem.readOnly}`,
    '  tmpfs_mounts:',
    ...policy.filesystem.tmpfsMounts.map(m => `    - "${m}"`),
    '  allowed_paths:',
    ...policy.filesystem.allowedPaths.map(p => `    - "${p}"`),
    '',
    'network:',
    `  egress: ${policy.network.egress}`,
    `  dns_policy: ${policy.network.dnsPolicy}`,
  ];

  if (policy.network.allowedHosts?.length) {
    lines.push('  allowed_hosts:');
    for (const host of policy.network.allowedHosts) {
      lines.push(`    - "${host}"`);
    }
  }

  lines.push(
    '',
    'process:',
    '  capabilities: []',
    `  seccomp: ${policy.process.seccomp}`,
    `  pids_limit: ${policy.process.pidsLimit}`,
    `  no_new_privileges: ${policy.process.noNewPrivileges}`,
    '',
    'inference:',
    `  privacy_router: ${policy.inference.privacyRouter}`,
    `  strip_credentials: ${policy.inference.stripCredentials}`,
  );

  if (policy.inference.allowedProviders?.length) {
    lines.push('  allowed_providers:');
    for (const p of policy.inference.allowedProviders) {
      lines.push(`    - "${p}"`);
    }
  }

  return lines.join('\n') + '\n';
}

